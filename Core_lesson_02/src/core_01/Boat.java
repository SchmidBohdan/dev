package core_01;

public class Boat {

	private String brend;
	
	private Engine engine;

	public Boat(String brend) {
		super();
		this.brend = brend;
	}

	public String getBrend() {
		return brend;
	}

	public void setBrend(String brend) {
		this.brend = brend;
	}

	public Engine getEngine() {
		return engine;
	}

	public void setEngine(Engine engine) {
		this.engine = engine;
	}
	
	
	
	
}
